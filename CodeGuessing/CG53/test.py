from entry import entry

def test(drawing, expected):
    if expected == False:
        expected = "nahhh"
    else:
        expected = "legit frfr"

    drawing = '\n'.join(drawing)
    if entry(drawing) == expected:
        print("Passed")
    else:
        print(f"Failed: Expected {expected} for:")
        for line in drawing.split('\n'):
            print(line)


